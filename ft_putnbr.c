#include "ft_printf.h"

void	ft_putnbr(int num)
{
	int		i;

	if(num == -2147483648)
		ft_putstr("2147483648");
	if (num < 0)
	{
		num = -num;
		ft_putchar('-');
	}
	if (num > 9)
	{
		i = num / 10;
		num -= 10 * i;
		ft_putnbr(i);
	}
	ft_putchar('0' + num);
}
